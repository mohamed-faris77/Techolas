let fruits=["Apple","Mango","Pome"]
console.log(fruits)

fruits.splice(0,0,"Orange") // add orange to 0 th index and no elements will be deleted
console.log(fruits)

fruits.splice(1,0,"Banana") // add banana to 0 th index and no elements will be deleted
console.log(fruits)

fruits.splice(0,2,"cherry") // cherry will be added to 0th index and from 0th index 2 elements will be deleted 
console.log(fruits)

